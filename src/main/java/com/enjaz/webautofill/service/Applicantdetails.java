package com.enjaz.webautofill.service;

public class Applicantdetails {

	private String passportNumber;
	private String enjazFee;
	private String medicalFee;
	private String visaFee;
	private String applicantId;
	private String email;
	private String contactNo;

	public String getPassportNumber() {
		return passportNumber;
	}

	public void setPassportNumber(String passportNumber) {
		this.passportNumber = passportNumber;
	}

	public String getEnjazFee() {
		return enjazFee;
	}

	public void setEnjazFee(String enjazFee) {
		this.enjazFee = enjazFee;
	}

	public String getMedicalFee() {
		return medicalFee;
	}

	public void setMedicalFee(String medicalFee) {
		this.medicalFee = medicalFee;
	}

	public String getVisaFee() {
		return visaFee;
	}

	public void setVisaFee(String visaFee) {
		this.visaFee = visaFee;
	}

	public String getApplicantId() {
		return applicantId;
	}

	public void setApplicantId(String applicantId) {
		this.applicantId = applicantId;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getContactNo() {
		return contactNo;
	}

	public void setContactNo(String contactNo) {
		this.contactNo = contactNo;
	}
}
